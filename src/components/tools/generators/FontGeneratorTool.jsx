const NORMAL = "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz";

function convertText(text, targetMap) {
	const chars = [...targetMap];
	return [...text]
		.map((ch) => {
			const idx = NORMAL.indexOf(ch);
			return idx >= 0 && idx < chars.length ? chars[idx] : ch;
		})
		.join("");
}

export default function FontGeneratorTool() {
	const [input, setInput] = useState("Hello World");

	const converted = useMemo(() => {
		return Object.entries(UNICODE_MAPS).map(([name, map]) => ({
			name,
			text: convertText(input, map),
		}));
	}, [input]);

	return (
		<Card>
			<CardHeader>
				<CardTitle className="text-lg flex items-center gap-2">
					<Type className="h-5 w-5 text-primary" />
					Fancy Font Generator
				</CardTitle>
			</CardHeader>
			<CardContent className="space-y-4">
				<Textarea
					placeholder="Type your text here..."
					value={input}
					onChange={(e) => setInput(e.target.value)}
					className="min-h-[80px] text-lg"
				/>
				<div className="grid gap-2">
					{converted.map(({ name, text }) => (
						<div
							key={name}
							className="flex items-center justify-between p-3 rounded-lg bg-muted/40 border border-border/50 hover:bg-muted/60 transition-colors cursor-pointer group"
							onClick={() => {
								navigator.clipboard.writeText(text);
								toast.success(`Copied ${name} style!`);
							}}
						>
							<div className="flex-1 min-w-0">
								<span className="text-xs font-medium text-muted-foreground block mb-1">{name}</span>
								<span className="text-base break-all">{text || "..."}</span>
							</div>
							<Button variant="ghost" size="sm" className="opacity-0 group-hover:opacity-100 transition-opacity ml-2 shrink-0">
								<Copy className="h-3.5 w-3.5" />
							</Button>
						</div>
					))}
				</div>
			</CardContent>
		</Card>
	);
}
